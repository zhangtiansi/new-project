<h3>私人房规则</h3>
<div style="text-align: left">
<p>1. 所有玩家都可以开设私人房。</p>
<p>2. 开设私人房每小时自动扣除10万金币作为开房费用。</p>
<p>3. 开设好房间后，房主不用强制留在私人房中。离开房间，哪怕房间没人，房间也会持续存在，直到开设时间结束。</p>
<p>4. 开设时间到了后，私人房会消失，需要重新开设。</p>
<p>5. 在私人房中，不受发呆时间超过90秒被踢出的规则限制，可无限停留，直到房间时间到。</p>
<p>6. 若在游戏中，房间时间到，则已开局的游戏不受影响，但准备下一局游戏时，出现提示“该房间时间到，无法开始游戏”</p>
</div>