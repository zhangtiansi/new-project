<div>3 、用户有权在游戏室内依据游戏规则享受快乐三张牌提供的游戏服务。</div>
<div>二、用户行为规则</div>
<div>4 、用户须严格遵守本服务条款、《快乐三张牌游戏服务条款》及快乐三张牌发布的有关游戏管理的公告，独立承担其在游戏活动中所可能产生的法律责任。</div>
