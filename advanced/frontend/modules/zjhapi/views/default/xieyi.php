<div>6 、用户在发现快乐三张牌可能存在系统漏洞时，须及时告知快乐三张牌官方网站，并不得从事任何利用系统漏洞的行为。</div>
<div>7 、用户承诺接受快乐三张牌对其游戏数据进行分析，当快乐三张牌发现其数据异常时，快乐三张牌可视其为作弊。</div>
<div>8 、用户不得利用任何方式进行偷盗或买卖账号、金币、级别以及积分等快乐三张牌游戏数据的活动。除快乐三张牌游戏明示提供的虚拟财产转移服务外，用户不得利用游戏输赢等任何方式直接或变相进行虚拟财产转移。</div>
<div>9 、用户不得冒充快乐三张牌或快乐三张牌工作人员发布信息；不得发布没有原因或理由的重复性垃圾信息以及内容与事实有明显差别的信息。</div>
<div>10 、未经当事人许可，用户不得发布其他用户以及快乐三张牌游戏管理人员的个人信息资料。</div>
<div>11、用户不得发布明显带有对快乐三张牌及其服务负面宣传、损害快乐三张牌声誉或商业利益的信息；不得发布明显带有对快乐三张牌非讨论性、恶意攻击性或错误讨论性的信息；不得发布影响快乐三张牌游戏聊天秩序的信息。</div>
<div>12 、用户不得发布任何带有个人恩怨性质的文章；不得发布侮辱、诽谤他人或者涉及他人隐私的信息；不得发布侵害他人知识产权等合法权益的信息。</div>
<div>13 、用户应服从快乐三张牌对游戏的管理，不得从事侮辱、诽谤、恶意攻击等损快乐三张牌游戏之受雇人、受托人、代理人及其他相关履行辅助人合法权益的行为。</div>
